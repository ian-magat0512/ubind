namespace UBind.Domain.NumberGenerators
{
    /// <summary>
    /// Specifies a method for generating unique numbers.
    /// </summary>
    /// <remarks>
    /// This is used to distinguish unique seed sequences.
    /// Different methods use their own seed sequence, so seeds are not guarenteed to be unique across methods.
    /// .</remarks>
    public enum NumberObfuscationMethod
    {
        /// <summary>
        /// Numbers are just integers, and each new number is the previous number plus one.
        /// </summary>
        None = 0,

        /// <summary>
        /// Unique number should be autogenerated as a six letter code.
        /// </summary>
        /// <remarks>Used for quote numbers.</remarks>
        SixDigitAlphabetic = 1,

        /// <summary>
        /// Unique number should be autogenerated as a six digit number,.
        /// </summary>
        /// <remarks>Used for DEFT CRN numbers in PSC due to WinBEAT reconciliation requirements.</remarks>
        SixDigitNonSequentialNumber = 2,

        /// <summary>
        /// Unique number should be autogenerated as a ten digit number,.
        /// </summary>
        /// <remarks>Used for DEFT CRN numbers.</remarks>
        TenDigitNonSequentialNumber = 3,

        /// <summary>
        /// Unique number should be autogenerated as an eight digit number.
        /// </summary>
        EightDigitAlphaNumeric = 4,
    }
}
