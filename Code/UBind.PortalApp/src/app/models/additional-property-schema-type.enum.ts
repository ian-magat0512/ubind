/**
 * Enum representing the schema types for additional property definitions.
 * - `None`: No schema defined.
 * - `OptionList`: The default JSON schema.
 * - `Custom`: A schema type that the user can define.
 */
export enum AdditionalPropertyDefinitionSchemaTypeEnum {
    None = 'None',
    OptionList = 'OptionList',
    Custom = 'Custom'
}
